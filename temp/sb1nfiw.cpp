#include <iostream>
using namespace std;

int main() {
    int a, b;
    
    // Ask for user input
    cout << "Enter two numbers: ";
    cin >> a >> b;
    
    // Calculate and print the sum
    int sum = a + b;
    cout << "The sum of " << a << " and " << b << " is: " << sum << endl;

    return 0;
}
